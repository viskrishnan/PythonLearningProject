# Task 1: Perform Basic Mathematical Operations

firstNum = input("Enter the first number: ")
firstNum = int(firstNum)
secNum = input("Enter the second number: ")
secNum = int(secNum)

Add = firstNum + secNum
Sub = secNum - firstNum
Mul = firstNum * secNum
Div = secNum / firstNum
Add = int(Add)
Sub = int(Sub)
Mul = int(Mul)
Div = float(Div)

print("Addition: ", Add)
print("Subtraction: ", Sub)
print("Multiplication: ",Mul)
print("Division: ",Div)